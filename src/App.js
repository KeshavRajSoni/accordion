function Accordion({ data }) {
  const [isOpen, setIsOpen] = useState(null);
  return <div className="accordion">
    {
      data.map((el, i) => <AccordionItem isOpen={isOpen} onIsOpen={setIsOpen} title={el.title} num={i} >
        {el.text}
        <br></br>
        <p>~keshav</p>
      </AccordionItem>)
    }
  </div>;
}

function AccordionItem({ num, title, children, isOpen, onIsOpen }) {

  function handelToggle() {
    onIsOpen(isOpen => num);
  }
  return <div className={`item ${isOpen === num ? "open" : ""}`} onClick={handelToggle}>
    <p className="number">{num < 9 ? `0${num + 1}` : `${num + 1}`}</p>
    <p className="title">{title}</p>
    <p className="icon">{isOpen ? '-' : '+'}</p>
    {isOpen === num && <div className="content-box">{children}</div>}
  </div>
}
